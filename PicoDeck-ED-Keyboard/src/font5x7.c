const uint8_t *font5x7_glyph(char character) {
    if (character >= 'A' && character <= 'Z') {
        return glyph_letters[(unsigned int)(character - 'A')];
    }
    if (character >= '0' && character <= '9') {
        return glyph_digits[(unsigned int)(character - '0')];
    }
    if (character == '%') {
        return glyph_percent;
    }
    return glyph_space;
}

